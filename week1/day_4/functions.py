## Functions
# Functions adalah sebuah code block atau code yang diisolasi yang bisa kita panggil sesuai kebutuhan kita
# referensi https://www.learnpython.org/en/Functions

## contoh
def hello_world():
    print("hello world!")
hello_world()

## penjelasan
# nama function diatas adalah hello_world()
# cara memanggil function diatas dengan cara ketik hello_world()
# hello_world() bisa dipanggil/ ditulis beberapa kali untuk melakukan pemanggilan function

## soal
# buatlah sebuah function yang digunakan untuk melakukan print sebuah stirng "panggil aku"
# panggil function tersebut sebanyak 5 kali
