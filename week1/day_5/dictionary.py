## Dictionary
# Dictionary adalah sebuah tipe variable yang bisa menyimpan variable lain. 
# Berbeda dengan list, dictionary menggunakan pembatas {}, sedangkan list menggunakan []
# Selain itu, dictionary bisa terdiri dari key dan value

## contoh 
my_dict = {"nama":"candra", "umur":30}

## penjelasan
# key = "nama" & "umur"
# value = "candra" & 30

## soal
# buatlah sebuah dictionary, lalu masukkan value ke dalam sebuah string berbentuk kalimat, misal "nama saya {nama} & umur {umur}" (kata kunci=get, dictionary)  
